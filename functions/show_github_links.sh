#!/bin/bash

show_github_links() {
  echo -e "${GREEN}**********************************${NC}"
  echo -e "${GREEN}*${NC} ${YELLOW}Nexos CLI GitHub Link:${NC} ${BLUE}https://github.com/nexolinux/nexos-cli${NC}"
  echo -e "${GREEN}*${NC} ${YELLOW}Nexos GitHub Link:${NC} ${BLUE}https://github.com/nexolinux/nexos${NC}"
  echo -e "${GREEN}*${NC} ${YELLOW}Nexus Official GitHub Link:${NC} ${BLUE}https://github.com/n3xusss/${NC}"
  echo -e "${GREEN}**********************************${NC}"
  read -p "$(echo -e ${YELLOW}"Press [Enter] to go back to the main menu...${NC}")"

}
